public class Nested_if {

	public static void main(String[] args) {
		
			 int num = 20;
			 
			 if (num >= 10) {
			   
				 System.out.println("Num is more than 10.");
			    
				 if (num > 15 ){
			      
					 System.out.println("Num is also more than 15.");
			     }
			  }
			 else 
			 {
				  System.out.println("Num is less than 10.");
			  }
			}
	}


